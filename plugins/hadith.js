export const command = {
    name: 'حديث',
    category: 'إسلاميات',
    async execute(sock, from, msg, args) {
        const ahadith = [
            "قَالَ رَسُولُ اللَّه ﷺ: «مَنْ سَلَكَ طَرِيقًا يَلْتَمِسُ فِيهِ عِلْمًا، سَهَّلَ اللَّهُ لَهُ بِهِ طَرِيقًا إِلَى الْجَنَّةِ» [رواه مسلم]",
            "قَالَ رَسُولُ اللَّه ﷺ: «إِنَّمَا الأَعْمَالُ بِالنِّيَّاتِ، وَإِنَّمَا لِكُلِّ امْرِئٍ مَا نَوَى» [رواه البخاري ومسلم]",
            "قَالَ رَسُولُ اللَّه ﷺ: «الْمُؤْمِنُ الْقَوِيُّ خَيْرٌ وَأَحَبُّ إِلَى اللَّهِ مِنَ الْمُؤْمِنِ الضَّعِيفِ، وَفِي كُلٍّ خَيْرٌ» [رواه مسلم]",
            "قَالَ رَسُولُ اللَّه ﷺ: «مَنْ كَانَ يُؤْمِنُ بِاللَّهِ وَالْيَوْمِ الآخِرِ فَلْيَقُلْ خَيْرًا أَوْ لِيَصْمُتْ» [رواه البخاري ومسلم]",
            "قَالَ رَسُولُ اللَّه ﷺ: «خَيْرُكُمْ مَنْ تَعَلَّمَ الْقُرْآنَ وَعَلَّمَهُ» [رواه البخاري]",
            "قَالَ رَسُولُ اللَّه ﷺ: «الدِّينُ النَّصِيحَةُ» [رواه مسلم]",
            "قَالَ رَسُولُ اللَّه ﷺ: «تَبَسُّمُكَ فِي وَجْهِ أَخِيكَ لَكَ صَدَقَةٌ» [رواه الترمذي]",
            "قَالَ رَسُولُ اللَّه ﷺ: «لا يُؤْمِنُ أَحَدُكُمْ حَتَّى يُحِبَّ لأَخِيهِ مَا يُحِبُّ لِنَفْسِهِ» [رواه البخاري ومسلم]",
            "قَالَ رَسُولُ اللَّه ﷺ: «مَنْ صَلَّى عَلَيَّ صَلَاةً صَلَّى اللَّهُ عَلَيْهِ بِهَا عَشْرًا» [رواه مسلم]",
            "قَالَ رَسُولُ اللَّه ﷺ: «كَلِمَتَانِ خَفِيفَتَانِ عَلَى اللِّسَانِ، ثَقِيلَتَانِ فِي الْمِيزَانِ، حَبِيبَتَانِ إِلَى الرَّحْمَنِ: سُبْحَانَ اللَّهِ وَبِحَمْدِهِ، سُبْحَانَ اللَّهِ الْعَظِيمِ»"
        ];

        // اختيار حديث عشوائي من المصفوفة
        const randomHadith = ahadith[Math.floor(Math.random() * ahadith.length)];

        await sock.sendMessage(from, { 
            text: `*📜┇ حـديـث شـريـف*\n\n${randomHadith}` 
        });
    }
};

